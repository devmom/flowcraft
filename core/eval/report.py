"""Evaluation report generator (Markdown + JSON) — bilingual EN/ZH output."""
from __future__ import annotations

import json
import logging
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

logger = logging.getLogger(__name__)


class EvalReportGenerator:
    """Generates Markdown and JSON evaluation reports in English and Chinese."""

    DIMENSION_LABELS = {
        "state_transition": "State Transition",
        "structure": "Structure Completeness",
        "security": "Security Compliance",
        "chain": "Call Chain Integrity",
        "efficiency": "Efficiency",
        "llm_judge": "LLM Judge (Output Quality)",
    }

    DIMENSION_LABELS_ZH = {
        "state_transition": "状态转移完整性",
        "structure": "结构完整性",
        "security": "安全合规",
        "chain": "调用链完整性",
        "efficiency": "执行效率",
        "llm_judge": "LLM 评判（输出质量）",
    }

    DIMENSION_ORDER = [
        "state_transition", "structure", "security", "chain",
        "efficiency", "llm_judge",
    ]

    REGRESSION_VERDICT_ZH = {
        "PASS": "通过",
        "WARN": "警告",
        "FAIL": "失败",
        "NO_BASELINE": "无基线",
    }

    def generate(self, results: dict, regression: dict | None,
                 output_dir: Path) -> Path:
        """Generate evaluation report in Markdown (EN + ZH) and JSON.

        Args:
            results: Aggregated evaluation results.
            regression: Regression detection results (or None).
            output_dir: Directory to write reports.

        Returns:
            Path to the report directory.
        """
        timestamp = datetime.now(timezone.utc).strftime("%Y%m%d_%H%M%S")
        report_dir = output_dir / timestamp
        report_dir.mkdir(parents=True, exist_ok=True)

        # Save raw JSON
        raw_path = report_dir / "summary.json"
        raw_data = {
            "timestamp": datetime.now(timezone.utc).isoformat(),
            "results": results,
            "regression": regression,
        }
        with open(raw_path, 'w', encoding='utf-8') as f:
            json.dump(raw_data, f, indent=2, ensure_ascii=False, default=str)

        # Save per-case details
        per_case = results.get("per_case", [])
        if per_case:
            cases_path = report_dir / "per_case.json"
            with open(cases_path, 'w', encoding='utf-8') as f:
                json.dump(per_case, f, indent=2, ensure_ascii=False, default=str)

        # Generate English Markdown report
        md_path = report_dir / "report.md"
        md = self._build_markdown(results, regression)
        with open(md_path, 'w', encoding='utf-8') as f:
            f.write(md)

        # Generate Chinese Markdown report
        md_zh_path = report_dir / "report_zh.md"
        md_zh = self._build_markdown_zh(results, regression)
        with open(md_zh_path, 'w', encoding='utf-8') as f:
            f.write(md_zh)

        logger.info("Report generated: %s", report_dir)
        return report_dir

    # ── English report ──────────────────────────────────────────

    def _build_markdown(self, results: dict,
                        regression: dict | None) -> str:
        """Build English Markdown report content."""
        ts = datetime.now(timezone.utc).isoformat()
        return self._build_markdown_core(
            results, regression, ts,
            title="FlowCraft Agent Evaluation Report",
            generated_label="Generated",
            exec_summary="Executive Summary",
            overall_score_label="Overall Score",
            total_cases_label="Total Cases",
            passed_label="Passed",
            failed_label="Failed",
            regression_label="Regression Check",
            dim_breakdown="Dimension Breakdown",
            dim_header=("Dimension", "Mean", "Median", "Pass Rate", "Min", "Max"),
            score_viz="Score Visualization",
            failed_cases="Failed Cases",
            case_id_label="Case ID",
            description_label="Description",
            score_label="Score",
            main_issue_label="Main Issue",
            unknown_issue="Unknown",
            efficiency_label="Efficiency Summary",
            efficiency_text="Efficiency metrics from rule-based evaluation:",
            footer="Report generated by FlowCraft Eval Engine",
            metric_label="Metric",
            value_label="Value",
            colon=":",
        )

    # ── Chinese report ──────────────────────────────────────────

    def _build_markdown_zh(self, results: dict,
                           regression: dict | None) -> str:
        """Build Chinese Markdown report content."""
        ts = datetime.now(timezone.utc).isoformat()
        return self._build_markdown_core(
            results, regression, ts,
            title="FlowCraft Agent 执行效果评估报告",
            generated_label="生成时间",
            exec_summary="评估概览",
            overall_score_label="综合评分",
            total_cases_label="总用例数",
            passed_label="通过",
            failed_label="未通过",
            regression_label="回归检测",
            dim_breakdown="维度明细",
            dim_header=("维度", "均值", "中位数", "通过率", "最低", "最高"),
            score_viz="分数可视化",
            failed_cases="未通过用例",
            case_id_label="用例ID",
            description_label="描述",
            score_label="评分",
            main_issue_label="主要问题",
            unknown_issue="未知",
            efficiency_label="效率摘要",
            efficiency_text="基于规则评估的效率指标：",
            footer="本报告由 FlowCraft Eval Engine 生成",
            metric_label="指标",
            value_label="数值",
            colon="：",
        )

    # ── Core builder (language-neutral skeleton) ─────────────────

    def _build_markdown_core(
        self,
        results: dict,
        regression: dict | None,
        timestamp: str,
        title: str,
        generated_label: str,
        exec_summary: str,
        overall_score_label: str,
        total_cases_label: str,
        passed_label: str,
        failed_label: str,
        regression_label: str,
        dim_breakdown: str,
        dim_header: tuple[str, ...],
        score_viz: str,
        failed_cases: str,
        case_id_label: str,
        description_label: str,
        score_label: str,
        main_issue_label: str,
        unknown_issue: str,
        efficiency_label: str,
        efficiency_text: str,
        footer: str,
        metric_label: str = "Metric",
        value_label: str = "Value",
        colon: str = ":",
    ) -> str:
        """Build language-independent Markdown report with localized labels."""
        lines: list[str] = []
        lines.append(f"# {title}")
        lines.append(f"**{generated_label}{colon}** {timestamp}")
        lines.append("")

        # Executive Summary
        lines.append(f"## {exec_summary}")
        overall = results.get("overall_score", 0)
        case_count = results.get("case_count", 0)
        pass_count = results.get("pass_count", 0)
        fail_count = case_count - pass_count

        lines.append(f"| {metric_label} | {value_label} |")
        lines.append(f"|--------|-------|")
        lines.append(f"| {overall_score_label} | **{overall:.1f} / 5.0** |")
        lines.append(f"| {total_cases_label} | {case_count} |")
        lines.append(
            f"| {passed_label} | {pass_count} "
            f"({pass_count / max(case_count, 1) * 100:.0f}%) |")
        lines.append(
            f"| {failed_label} | {fail_count} "
            f"({fail_count / max(case_count, 1) * 100:.0f}%) |")
        lines.append("")

        # Regression
        if regression and regression.get("verdict") != "NO_BASELINE":
            verdict = regression["verdict"]
            icon = {"PASS": "PASS", "WARN": "WARN", "FAIL": "FAIL"}.get(verdict, "?")
            lines.append(f"**{regression_label}：** {icon} ({verdict})")
            for reg in regression.get("regressions", []):
                lines.append(
                    f"- {reg['dimension']}.{reg['metric']}: "
                    f"{reg['baseline']} → {reg['current']} "
                    f"(delta: {reg['delta']:+.2f}) [{reg['severity']}]")
            lines.append("")

        # Dimension Breakdown
        lines.append(f"## {dim_breakdown}")
        headers = " | ".join(dim_header)
        lines.append(f"| {headers} |")
        lines.append("|" + "|".join(["-" * (len(h) + 2) for h in dim_header]) + "|")
        dims = results.get("dimensions", {})
        for dim_name in self.DIMENSION_ORDER:
            dim_data = dims.get(dim_name, {})
            if not dim_data:
                continue
            label = self.DIMENSION_LABELS.get(dim_name, dim_name)
            lines.append(
                f"| {label} | {dim_data.get('mean', 0):.2f} | "
                f"{dim_data.get('median', 0):.2f} | "
                f"{dim_data.get('pass_rate', 0):.0%} | "
                f"{dim_data.get('min', 0):.2f} | "
                f"{dim_data.get('max', 0):.2f} |"
            )
        lines.append("")

        # Score visualization
        lines.append(f"### {score_viz}")
        for dim_name in self.DIMENSION_ORDER:
            dim_data = dims.get(dim_name, {})
            if not dim_data:
                continue
            label = self.DIMENSION_LABELS.get(dim_name, dim_name)
            mean = dim_data.get("mean", 0)
            bar_len = int(mean / 5.0 * 20)
            bar = "[" + "#" * bar_len + "-" * (20 - bar_len) + "]"
            lines.append(f"- {label}: {mean:.1f}/5.0 {bar}")
        lines.append("")

        # Failed Cases
        per_case = results.get("per_case", [])
        failed = [c for c in per_case if c.get("overall_score", 5) < 3.0]
        if failed:
            lines.append(f"## {failed_cases}")
            lines.append(
                f"| {case_id_label} | {description_label} | "
                f"{score_label} | {main_issue_label} |")
            lines.append("|---------|-------------|-------|------------|")
            for case in failed[:15]:
                cid = case.get("case_id", "?")
                desc = case.get("description", "")[:60]
                score = case.get("overall_score", 0)
                weaknesses = case.get("llm_judge", {}).get("weaknesses", [])
                main_issue = weaknesses[0] if weaknesses else unknown_issue
                lines.append(
                    f"| {cid} | {desc} | {score:.1f} | "
                    f"{main_issue[:80]} |")
            lines.append("")

        # Efficiency Summary
        efficiency = dims.get("efficiency", {})
        if efficiency:
            lines.append(f"## {efficiency_label}")
            lines.append(efficiency_text)
            lines.append("")

        lines.append("---")
        lines.append(f"*{footer}*")

        return "\n".join(lines)


REGRESSION_VERDICT_MAP: dict[str, str] = {
    "PASS": "PASS / 通过",
    "WARN": "WARN / 警告",
    "FAIL": "FAIL / 失败",
    "NO_BASELINE": "NO_BASELINE / 无基线",
}


def quick_report(results: dict, regression: dict | None = None) -> str:
    """Generate a one-line summary for quick feedback (English)."""
    overall = results.get("overall_score", 0)
    case_count = results.get("case_count", 0)
    pass_count = results.get("pass_count", 0)
    reg = regression or {}
    reg_verdict = reg.get("verdict", "N/A")
    return (
        f"Eval: {overall:.1f}/5.0 | "
        f"{pass_count}/{case_count} passed | "
        f"Regression: {reg_verdict}"
    )


def quick_report_zh(results: dict, regression: dict | None = None) -> str:
    """Generate a one-line summary for quick feedback (Chinese)."""
    overall = results.get("overall_score", 0)
    case_count = results.get("case_count", 0)
    pass_count = results.get("pass_count", 0)
    reg = regression or {}
    reg_verdict = reg.get("verdict", "N/A")
    reg_zh = REGRESSION_VERDICT_MAP.get(reg_verdict, reg_verdict)
    return (
        f"评估: {overall:.1f}/5.0 | "
        f"{pass_count}/{case_count} 个通过 | "
        f"回归: {reg_zh}"
    )
